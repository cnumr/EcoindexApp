// #region Windows creation
export const createHelloWindow = () => {
    if (!hasShowWelcomeWindow() || process.env['WEBPACK_SERVE'] === 'true') {
        const displayHello = `displayHello.${convertVersion(packageJson.version)}`
        setWelcomeWindow(
            new BrowserWindow({
                width: 800,
                height: 700,
                icon: '/assets/app-ico.png',
                webPreferences: {
                    preload: HELLO_WINDOW_PRELOAD_WEBPACK_ENTRY,
                },
                parent: getMainWindow(),
                modal: true,
                show:
                    !store.get(displayHello) &&
                    store.get(displayHello) !== true,
            })
        )
        getWelcomeWindow().loadURL(HELLO_WINDOW_WEBPACK_ENTRY)
    } else {
        setHasShowedWelcomeWindow(true)
    }
}

/**
 * Helpers, Main Window Creation
 */
const createMainWindow = (): void => {
    // Create the browser window.
    setMainWindow(
        new BrowserWindow({
            width: 1000,
            height: 800,
            icon: '/assets/app-ico.png',
            webPreferences: {
                preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
            },
        })
    )

    // and load the index.html of the app.
    getMainWindow().loadURL(MAIN_WINDOW_WEBPACK_ENTRY)

    // createHelloWindow()

    i18nInit()

    _changeLanguage(store.get(`language`, `en`) as string)

    // Linux Updater
    const checkLinuxUpdater = async () => {
        const url =
            'https://api.github.com/repos/cnumr/EcoindexApp/releases/latest'
        const tags = await fetch(url).then((_) => _.json())
        const currentVersion = packageJson.version
        const lastVersion = tags['tag_name']
        if (currentVersion !== lastVersion) {
            const lastVersionURL = tags['html_url']
            mainLog.debug(`currentVersion`, currentVersion)
            mainLog.debug(`lastVersion`, lastVersion)
            mainLog.debug(`lastVersionURL`, lastVersionURL)
            mainLog.debug(`Update Needed!`)
            const linuxUpdate = new LinuxUpdate(lastVersion, lastVersionURL)
            getMainWindow().webContents.send(
                channels.ALERT_LINUX_UPDATE,
                linuxUpdate
            )
        }
    }

    if (process.platform === 'linux') {
        checkLinuxUpdater()
    }
    // Open the DevTools.
    // mainWindow.webContents.openDevTools({ mode: 'detach' })
}

