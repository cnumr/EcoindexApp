const i18nInit = () => {
    try {
        i18n.on('loaded', () => {
            try {
                i18n.changeLanguage('en')
                i18n.off('loaded')
            } catch (error) {
                //
            }
        })
        i18n.on('languageChanged', (lng) => {
            _changeLanguage(lng)
        })
    } catch (error) {
        mainLog.error(`i18n`, error)
    }
    // read language in store
    i18n.changeLanguage(store.get(`language`, `fr`) as string)
}
